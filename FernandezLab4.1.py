##Variable input
budget = float(input("Enter your monthly budget: "))
total_expenses = 0
while True:
    expense = float(input("Enter an expense (or 0 to quit): "))
    if expense == 0:
        break
    total_expenses += expense
##Determine if the user is over or under budget
if total_expenses > budget:
    print(f"You are ${total_expenses - budget:.2f} over budget")
    print("PLAN BETTER NEXT TIME!")
